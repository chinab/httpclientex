package cn.sckj.jsoup.pojo;

public class DrugURL {
	private int id;
	private String URL_ID;
	private String URL_STR;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the uRL_ID
	 */
	public String getURL_ID() {
		return URL_ID;
	}
	/**
	 * @param uRL_ID the uRL_ID to set
	 */
	public void setURL_ID(String uRL_ID) {
		URL_ID = uRL_ID;
	}
	/**
	 * @return the uRL_STR
	 */
	public String getURL_STR() {
		return URL_STR;
	}
	/**
	 * @param uRL_STR the uRL_STR to set
	 */
	public void setURL_STR(String uRL_STR) {
		URL_STR = uRL_STR;
	}
	
}
